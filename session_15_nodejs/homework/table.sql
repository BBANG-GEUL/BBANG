
/*1. 고객정보 테이블 */
DROP TABLE IF EXISTS USERS;

CREATE TABLE USERS (
  ID int(4) NOT NULL,
  UID varchar(30) NOT NULL,
  NAME varchar(5) NOT NULL,
  EMAIL varchar(30) NOT NULL,
  PET_NAME varchar(10) NOT NULL,
  PET_TYPE_ID int(2) NOT NULL,
  PHONE varchar(15) NOT NULL,
  MOBILE varchar(15) NOT NULL,
  ACTIVE enum('ACTIVE', 'INACTIVE') NOT NULL,
  CREATED_AT timestamp NOT NULL,
  UPDATED_AT timestamp NOT NULL
);


/*2. 펫종류 테이블 */
DROP TABLE IF EXISTS PET_TYPE;

CREATE TABLE PET_TYPE (
  ID int(3) NOT NULL,
  TITLE varchar(10) NOT NULL,
  STATUS enum('PUBLISHED', 'UNPUBLISHED') NOT NULL,
  CREATED_AT timestamp NOT NULL,
  UPDATED_AT timestamp NOT NULL
);


/*3. 펫이 받을 서비스 종류 테이블 */
DROP TABLE IF EXISTS SERVICES;

CREATE TABLE SERVICES (
  ID int(3) NOT NULL,
  TITLE varchar(8) NOT NULL,
  STATUS enum('PUBLISHED', 'UNPUBLISHED') NOT NULL,
  CREATED_AT timestamp NOT NULL,
  UPDATED_AT timestamp NOT NULL
);


/*4. 관리자 테이블 */
DROP TABLE IF EXISTS MEMBERS;

CREATE TABLE MEMBERS (
  ID int(4) NOT NULL,
  UID varchar(4) NOT NULL,
  USERNAME varchar(8) NOT NULL,
  PASSWORD varchar(10) NOT NULL,
  PASSWORD_REMEMBER_TOKEN varchar(1) default NULL,
  TYPE enum('OWNER', 'STAFF') NOT NULL,
  CREATED_AT timestamp NOT NULL,
  UPDATED_AT timestamp NOT NULL
);


/*5. 예약 정보 테이블 */
DROP TABLE IF EXISTS BOOGKINGS;

CREATE TABLE BOOGKINGS (
  ID int(4) NOT NULL,
  UID varchar(4) NOT NULL,
  USER_ID int(4) NOT NULL,
  MEMBER_ID int(4) NOT NULL,
  SERVICE_ID int(3) NOT NULL,
  BOOKING_TIME time NOT NULL,
  COMMENTS text default NULL,
  STATUS enum('PENDING', 'CANCEL', 'DONE') NOT NULL,
  CREATED_AT timestamp NOT NULL,
  UPDATED_AT timestamp NOT NULL
);




